# Tipo de Bool ( Boolean)
# Ao questionar algo em um programa,
# Só existe duas reposta possiveis:
# Sim (True) ou Não (False),
# Existem Vários operadores para "questionar".
# Dentre eles o ==, é que é um operador lógico que
# Questiona se um valoe é igual a outro
print(10==10) #Sim-> (True)
print(10==11) # Não-> (False)
print(type(10==10)) 

